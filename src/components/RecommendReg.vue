              <el-input :type="delivery ?  'text' : 'password'" v-model="ruleForm.password"
                        placeholder="请输入密码"></el-input>
              <el-switch v-model="delivery"></el-switch>
            </div>
          </div>
          <div class="agreement">
            <el-checkbox v-model="ruleForm.checked"></el-checkbox>
            <span>已阅读并同意《用户服务协议》</span>
          </div>
          <div class="goto">
            <button @click="doRegister">注册</button>
          </div>
          <div class="behavior">
            <router-link to="/login">
              <p>已有账户， <span>直接登录>></span></p>
            </router-link>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script type="es6">
  import * as Constants from '../custom/constants'
  import { Toast } from 'mint-ui';
  import url from '../http/url.js'
  import {mapActions} from 'vuex'
  import {getUserInfoById} from "../http/getData";

  export default {
    name: "RecommendReg",
    data() {
      return {
        recommendUserId:'',
        recommender:{},
        delivery:false,
        ruleForm: {
          mobile: '',
          verify: '',
          password: '',
          checked:true
        }
      };
    },
    methods: {
      ...mapActions([
        'loginState',
        'setToken'
      ]),
      isWeiXin() {
        var ua = window.navigator.userAgent.toLowerCase();
        if (ua.match(/MicroMessenger/i) == 'micromessenger') {
          return true;
        } else {
          return false;
        }
      },
      async storeState(token){
        await localStorage.setItem(Constants.TOKEN, token)
        await  this.setToken(token)
        await  this.loginState(true)
      },
      goBack() {
        this.$router.back()
      },
      sendCode(formName) {
        if (this.ruleForm.mobile === '') {
          Toast({
            message: '手机号不能为空',
            position: 'middle',
            duration: 1000
          })
          return
        } else if (!(/^1(3|4|5|7|8)\d{9}$/.test(this.ruleForm.mobile))) {
          Toast('手机号码格式不正确')
          return
        }
        this.$refs.btnCode.disabled = true;
        this.axios.post(url.verify, {
          mobile: this.ruleForm.mobile,
          codeType: "0"
        }).then(response => {
          if (response.data.code === 0) {
            let btnCode = this.$refs.btnCode;
            let second = 60
            btnCode.innerHTML = second + 'S后重发'
            let timer = setInterval(() => {
              second--
              btnCode.innerHTML = second + 'S后重发'
              if (second <= 0) {
                clearInterval(timer)
                btnCode.innerHTML = '重发验证码'
                this.$refs.btnCode.disabled = false
              }
            }, 1000)
          } else if (response.data.code === 500) {
            Toast(response.data.msg);
          }
        }).catch(function (error) {});
      },
      doRegister() {
        if (this.ruleForm.mobile === ''){
          Toast({
            message: '手机号不能为空',
            position: 'middle',
            duration: 1000})
          return
        }else if(!(/^1(3|4|5|7|8)\d{9}$/.test(this.ruleForm.mobile))){
          Toast('手机号码格式不正确')
          return
        }
        if (this.ruleForm.password === '') {
          Toast({
            message: '密码不能为空',
            position: 'middle',
            duration: 1000
          });
          return false
        } else {
          if (!(/^[\w.]{6,20}$/.test(this.ruleForm.password))) {
            Toast({
              message: '密码长度需为6-20位',
              position: 'middle',
              duration: 1000
            });
            return false
          }
        }
        if (!this.ruleForm.checked) {
          Toast({
            message: '您尚未同意用户服务协议',
            position: 'middle',
            duration: 1000
          });
          return false
        }
        this.axios.post(url.reg, {
          mobile: this.ruleForm.mobile,
          password: this.ruleForm.password,
          code: this.ruleForm.verify,
          recommendUserId: this.recommendUserId,
          selected: this.ruleForm.checked ? "0" : "1"
        }).then(response => {
          if (response.data.code === 0) {
            console.log(response)
            let data = response.data
            let userId = response.data.result.userId
            this.storeState(data.result.token).then(() => {
              if(this.isWeiXin()){
                let authUrl = url.wxAuth + userId
                window.location.href = authUrl
              }else{
                this.$router.push('/mallindex')
              }
            })
          } else if (response.data.code === 500) {
            Toast(response.data.msg);
          }
        }).catch(function (error) {});
      }
    },
    mounted(){
      let {id} = this.$route.params
      this.recommendUserId = id
      getUserInfoById({
        id: id
      }).then(response=>{
        this.recommender = response.result
      })
    }
  }
</script>

