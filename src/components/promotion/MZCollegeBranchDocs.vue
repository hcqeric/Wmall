        <p>所谓情绪管理，不单单指控制自己不发脾气，而是以恰当的方式来表达情绪，使周围的人能够接受。专家指出，做好情绪管理，应学会制怒术、助人术、宣泄术、转移术、放松术等方法。</p>　　
        <p>&#8220;情绪管理&#8221;即是以最恰当的方式来表达情绪，如同亚里士多德所言：&#8220;任何人都会生气，这没什么难的，但要能适时适所，以适当方式对适当的对象恰如其分地生气，可就难上加难。&#8221;据此，情绪管理指的是要适时适所，对适当对象恰如其分表达情绪。情绪管理是一门学问，也是一种艺术，要掌控得恰到好处。因此要成为情绪的主人，必先觉察自我的情绪，并能觉察他人的情绪，进而能管理自我情绪，尤其要常保鲜活的心情面对人生。　　
        </p>
        <p>做好情绪管理 做情绪的主人</p>
          <p>善于控制、治理自身情绪的人，能够消除情绪的负效能，最大限度地开发情绪的正效能。</p>　　
        <p>1）选择情绪：一个愈懂得选择情绪的人，也就是愈能换个心情的人。如此其人生不仅愈丰富，而且每天的日子会过得较快乐。</p>　　
        <p>2）转移术（冷却或转移注意力）：有人问一位西藏高僧应如何处理愤怒，他答复说：&#8220;不要压抑，但也不要冲动行事。&#8221;换言之，一个人遇事立刻发泄怒气，将会使愤怒的情绪更加延长，倒不如先冷却一段时间，使心情平静下来后，再采取较理性的方法解决问题。因此，当一种需求受阻或者遭到挫折时，可以用满足另一种需求来代偿。也可以通过分散注意力，改变环境来转移情绪的指向。</p>
        <p>3）制怒术（或适度表达愤怒）：在遇到发怒的事情时，首先想想发怒有无道理，其次发怒后有何后果，然后想想是否有其它方式代替发怒。这样一想，你就可以变得冷静而情绪稳定。情感平淡，生命将枯躁而无味，太极端又会变成一种病态，如抑郁到了无兴趣，过度焦虑，怒不可遏，坐立不安都是病态。所以我们要如亚里士多德所强调&#8220;适时适所表达情绪&#8221;。</p>　　
        <p>4）助人术：多做善事，既可以给他人带来快乐，也可使自己心境坦然，具有较好的安全感。</p>　　
        <p>5）宣泄术（进行自我教导）：遇到不如意、不愉快的事情，可以通过做运动、读小说、听音乐、看电影、找朋友倾诉来宣泄自己不愉快的情绪，也可以大哭一场。或者改变自我情绪，增加自信心的另一种方法就是自己找一句座右铭或对自己说一些自我肯定的话，以激励自我。</p>　　
        <p>6）替代术（使用替代想法或理情治疗法）：理情治疗法强调理念、信念会主宰一个人的情绪。一旦产生不合理的信念，情绪会产生较大的波动。因此常保持良好或善意的理念，情绪也会较稳定。</p>　　
        <p>7）放松术：心情不佳时，可以通过循序渐进、自上而下放松全身，或者是通过自我催眠、自我按摩等方法使自己进入放松状态，然后面带微笑，想象曾经经历过的愉快情境，从而消除不良情绪。</p>　　
        <p>总之，情绪是可以得到有效管理的，只是很多人疏忽了这一点，而变成&#8220;情绪的奴隶&#8221;，在不良情绪中挣扎，自己很痛苦，也会把这种状态传染给周围的人。</p>
          <p>
            情绪对于个体来讲，是具有保护意义的，每种情绪都承担了相应的功能，帮助我们理解自己的内在需求。比如当我们感觉愤怒的时候，愤怒情绪是在提醒我们拒绝或者远离伤害性体验；当我们快乐的时候，快乐可以帮助我们与他人建立安全的关系；当我们悲伤的时候，提示我们内心的某些痛苦需要一个安全的释放通道，等等。所以，情绪本身是没有好坏之分的，有破坏性的是我们拒绝情绪所传达的需求，或者对情绪不恰当的管理方式。
          </p>
          <p>
            在一个人的生命历程中，情绪具有非常大的影响力。比如一个孩子如果从一出生，人格特质中就具有易于满足的特质，ta在与养育者的互动中，可能就会很容易出现满足的状态，而养育者在养育这个孩子的过程就会比较容易获得满足，从而养育者就会有更多的精力投入到与婴儿的互动中来，婴儿就会从这些互动中获得进一步的满足，这个满足可能会吸引照顾者将更多的精力投注到ta的身上，因此，婴儿与养育者之间就会形成良性互动。婴儿在这样的养育条件下，就有机会积累更多安全的，积极的情绪体验，这些体验也会帮助ta发展对关系的信任，有助于ta成人之后，在社会上具有更好的适应性，发展更好的人际关系，更多调动出自己的潜能，成为一个有能力的人。
          </p>
          <p
          >
            而一个孩子如果出生时就具有难养特质，可能就会使养育者在养育过程中变得非常焦虑，这些焦虑同样会被孩子感受到，而当一个孩子感受到父母的焦虑时，ta的内在世界可能就会变得非常恐惧，甚至是混乱。因为作为一个如此柔弱的孩子，ta还不具备代谢这些焦虑的功能，ta需要借助于养育者对这些焦虑的容纳能力来释放掉压力。容纳是指这样一个过程：孩子的各种焦虑表现出来，父母作为一个容器接收了这些情绪，但是这些情绪在父母的身体里进行了过滤，变得不那么具有伤害性，孩子在父母身上感受到父母可以很坦然的处理这些情绪而不是被情绪控制时，就会感受到安全。比如当孩子受伤时，孩子会害怕，如果父母很镇静，孩子就会觉得不必怕。而此时如果养育者也处于焦虑状态，则孩子的内在安全体验就会受阻，这种情况的发生甚至会影响到孩子人格的健康发展。所以，在养育孩子的过程中，父母内部处理情绪压力的能力，是非常重要的功能。孩子就是从这些感受中学到如何应对压力的，孩子成长中感受到的这些内容，也成为ta将来独自面对压力时的模版和资源库。
          </p>
          <p >情绪的表达方式从低级到高级有几个层次。
          </p>
          <p
            >
            <![if !supportLists]>&#183;&nbsp;<![endif]>第一级，躯体表达。现在大家都知道很多疾病是身心疾病，癌症，高血压，胃溃疡、免疫系统的疾病，等等。为什么明明是身体上的疾病，却说它跟心理沾了边了呢？因为这些疾病背后，是无法表达情绪的压力在做怪。当情绪不能得以表达时，它并不是就此消失了，而是成为各种压力积存在身体里，对身体器官进行攻击，最终以疾病的方式表达出来，心理学上将这种情况叫做躯体化。
          </p>
          <p
            >
