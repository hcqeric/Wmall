          </p>
          <p
            >
            <![if !supportLists]>&#183;&nbsp;<![endif]>第三级，语言表达。这是相对成熟的表达方式。我们能用语言清晰表达出自己的情绪，至少经历了感受情绪，评估关系的安全度，组织语言表达等等内在心理过程，所以，用语言表达出情绪，并不是件容易的事，很多人无法用语言表达，是因为对于关系没有足够的安全感，所以宁愿忍着也不能表达，而忍的结果，要么伤害自己，要么因为暴发而伤害对方。动力性（精神分析）心理咨询中很重要的工作，就是帮助求助者发展用语言表达的能力，因为一旦能够用语言表达出来，压力释放掉，就不必用行动去表达，也不必伤害到身体了。
          </p>
