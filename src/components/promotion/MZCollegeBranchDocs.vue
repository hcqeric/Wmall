            <![if !supportLists]>&#183;&nbsp;<![endif]>第四级，象征性表达。这是很成熟的一种表达方式，比如大地震中失去家园的孩子，用积木不断搭起一座房子，推倒，再重新搭起来，在这个过程中，孩子在用象征性的方式表达对于家园失去与重建的种种感受。顺便说一句，游戏对于孩子发展健康人格是非常有意义的。
